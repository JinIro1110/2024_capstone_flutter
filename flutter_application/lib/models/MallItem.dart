class MallItem {
  final String code;
  final String name;
  final String price;
  final String brand;
  final String category;
  final String mainCategory;
  final String subCategory;
  final String imageUrl;
  final String link;

  MallItem({
    required this.code,
    required this.name,
    required this.price,
    required this.brand,
    required this.category,
    required this.mainCategory,
    required this.subCategory,
    required this.imageUrl,
    required this.link,
  });
}